    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:

        try {
            if (tabla.getSelectedRow() == -1) {
                JOptionPane.showMessageDialog(null, "Tienes que selecionar un cliente de la tabla");
                return;
            }
            int id = lista.get(row).getId();
            String nombre = lista.get(row).getNombre();
            String correo = lista.get(row).getEmail();
            String telefono = lista.get(row).getTelefono();

            controlador.delete(id, nombre, correo, telefono);
            llenarTabla();
            borrarCampos();
        } catch (Exception e) {
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
        String nombre = txtNombre.getText();
        String correo = txtCorreo.getText();
        String telefono = txtTelefono.getText();
        try {
            if (nombre.equals("") && correo.equals("") && telefono.equals("")) {
                JOptionPane.showMessageDialog(null, "Debes llenar todo los campos");
                return;
            }
            controlador.save(nombre, correo, telefono);
            llenarTabla();
            borrarCampos();
        } catch (Exception e) {
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

